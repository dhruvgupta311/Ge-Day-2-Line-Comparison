// Point.java
public class Point {
    private int x;
    private int y;

    // Constructor to initialize the coordinates
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getter methods for x and y coordinates
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
